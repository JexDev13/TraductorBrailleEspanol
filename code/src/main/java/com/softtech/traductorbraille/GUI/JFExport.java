    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        String selectedOption = (String) jComboBoxOpciones.getSelectedItem();
        String selectedPath = jTextField1.getText();
        String fileName = jTextFieldNombreArchivo.getText();

        if (selectedPath.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione una carpeta para guardar el archivo.");
            return;
        }

        if (fileName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre para el archivo.");
            return;
        }

        File file = new File(selectedPath, fileName + "." + selectedOption);
        try {
            exportService.exportBraille(file, selectedOption, textToExport);
            JOptionPane.showMessageDialog(this, "Texto exportado como " + selectedOption.toUpperCase() + " exitosamente.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al exportar como " + selectedOption + ": " + ex.getMessage());
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void JButtonSelectFolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSelectFolderActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        centerFileChooserOnScreen(chooser); // Centra el JFileChooser en la pantalla
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();
            jTextField1.setText(selectedFile.getAbsolutePath());
        }
    }//GEN-LAST:event_JButtonSelectFolderActionPerformed

    // Método para centrar el JFileChooser en la pantalla
    private void centerFileChooserOnScreen(JFileChooser chooser) {
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension dialogSize = chooser.getSize();
        chooser.setLocation((screenSize.width - dialogSize.width) / 2, (screenSize.height - dialogSize.height) / 2);
    }

